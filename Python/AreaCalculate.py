# Circle 
r = float(input("Enter the Redius :"))
area1 = 3.1416 * r
print("Area of circle = ",area1)

# Triangle
b = float(input("Enter the Base :"))
h = float(input("Enter the Hight :"))
area2 = 0.5 * b * h
print("Area of triangle = ",area2)

# Trapezium
a = input("Enter the Length: ")
b = input("Enter the Wide: ")
h = float(input("Enter the Hight :"))
sum = int(a) + int(b)
area3 = 0.5 * sum * h
print("Area of Trapezium = ",area3)
